# –ò–º–ø–æ—Ä—Ç—ã
import os
import psycopg2                    # –ë–∏–±–ª–∏–æ—Ç–µ–∫–∞ –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å PostgreSQL
from psycopg2.extras import RealDictCursor  # –ö—É—Ä—Å–æ—Ä, –≤–æ–∑–≤—Ä–∞—â–∞—é—â–∏–π –¥–∞–Ω–Ω—ã–µ –≤ –≤–∏–¥–µ —Å–ª–æ–≤–∞—Ä—è
from dotenv import load_dotenv     # –î–ª—è –∑–∞–≥—Ä—É–∑–∫–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –∏–∑ .env —Ñ–∞–π–ª–∞
from contextlib import contextmanager  # –î–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –∫–æ–Ω—Ç–µ–∫—Å—Ç–Ω—ã—Ö –º–µ–Ω–µ–¥–∂–µ—Ä–æ–≤

# –ó–∞–≥—Ä—É–∂–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è –∏–∑ .env —Ñ–∞–π–ª–∞
load_dotenv()


class Database:
    """
    –ö–ª–∞—Å—Å –¥–ª—è —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ–º –∫ PostgreSQL.
    –û—Å–Ω–æ–≤–Ω–æ–π –∫–ª–∞—Å—Å, –∫–æ—Ç–æ—Ä—ã–π –∏–Ω–∫–∞–ø—Å—É–ª–∏—Ä—É–µ—Ç –≤—Å—é —Ä–∞–±–æ—Ç—É —Å –ë–î.
    """

    @staticmethod
    def get_connection():
        """
        –°–æ–∑–¥–∞–µ—Ç –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ –ë–î.

        Returns:
            psycopg2.connection: –û–±—ä–µ–∫—Ç –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ PostgreSQL
        """
        try:
            # –°–æ–±–∏—Ä–∞–µ–º –ø–∞—Ä–∞–º–µ—Ç—Ä—ã –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∏–∑ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è
            conn_params = {
                'dbname': os.getenv('DB_NAME', 'notebookk_db'),      # –ò–º—è –ë–î (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é 'notebookk_db')
                'user': os.getenv('DB_USER', 'postgres'),           # –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é 'postgres')
                'password': os.getenv('DB_PASSWORD', ''),           # –ü–∞—Ä–æ–ª—å (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é –ø—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞)
                'host': os.getenv('DB_HOST', 'localhost'),          # –•–æ—Å—Ç (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é 'localhost')
                'port': os.getenv('DB_PORT', '5432'),               # –ü–æ—Ä—Ç (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é 5432)
                'connect_timeout': 10,                               # –¢–∞–π–º–∞—É—Ç –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è 10 —Å–µ–∫—É–Ω–¥
                'sslmode': 'disable',                                # –û—Ç–∫–ª—é—á–∞–µ–º SSL (–¥–ª—è –ª–æ–∫–∞–ª—å–Ω–æ–π —Ä–∞–∑—Ä–∞–±–æ—Ç–∫–∏)
                'client_encoding': 'UTF8'                            # –ö–æ–¥–∏—Ä–æ–≤–∫–∞ UTF-8
            }

            # –û—Ç–ª–∞–¥–æ—á–Ω—ã–π –≤—ã–≤–æ–¥ –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è
            print(f"üîß –ü–∞—Ä–∞–º–µ—Ç—Ä—ã –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è: host={conn_params['host']}, port={conn_params['port']}, user={conn_params['user']}")

            # –°–æ–∑–¥–∞–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ —Å —É–∫–∞–∑–∞–Ω–Ω—ã–º–∏ –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º–∏
            conn = psycopg2.connect(**conn_params)
            print("‚úÖ –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ!")
            return conn

        except psycopg2.OperationalError as e:
            # –û—à–∏–±–∫–∞ –Ω–∞ —É—Ä–æ–≤–Ω–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è (—Å–µ—Ä–≤–µ—Ä –Ω–µ –¥–æ—Å—Ç—É–ø–µ–Ω, –Ω–µ–≤–µ—Ä–Ω—ã–µ —É—á–µ—Ç–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ –∏ —Ç.–¥.)
            print(f"‚ùå –û—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è: {e}")
            print("–ü—Ä–æ–≤–µ—Ä—å—Ç–µ:")
            print("1. –ó–∞–ø—É—â–µ–Ω –ª–∏ PostgreSQL (services.msc)")
            print("2. –ü—Ä–∞–≤–∏–ª—å–Ω—ã–π –ª–∏ –ø–∞—Ä–æ–ª—å –≤ .env")
            print("3. –ú–æ–∂–µ—Ç –ª–∏ localhost –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è (127.0.0.1 –≤–º–µ—Å—Ç–æ localhost)")
            raise  # –ü—Ä–æ–±—Ä–∞—Å—ã–≤–∞–µ–º –∏—Å–∫–ª—é—á–µ–Ω–∏–µ –¥–∞–ª—å—à–µ

        except Exception as e:
            # –õ—é–±–∞—è –¥—Ä—É–≥–∞—è –Ω–µ–æ–∂–∏–¥–∞–Ω–Ω–∞—è –æ—à–∏–±–∫–∞
            print(f"‚ùå –ù–µ–æ–∂–∏–¥–∞–Ω–Ω–∞—è –æ—à–∏–±–∫–∞: {e}")
            raise

    @staticmethod
    @contextmanager
    def get_cursor():
        """
        –ö–æ–Ω—Ç–µ–∫—Å—Ç–Ω—ã–π –º–µ–Ω–µ–¥–∂–µ—Ä –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –∫—É—Ä—Å–æ—Ä–æ–º.
        –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ —É–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –∂–∏–∑–Ω–µ–Ω–Ω—ã–º —Ü–∏–∫–ª–æ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ –ë–î.
        –ì–∞—Ä–∞–Ω—Ç–∏—Ä–æ–≤–∞—Ç—å, —á—Ç–æ —Å–æ–µ–¥–∏–Ω–µ–Ω–∏–µ –≤—Å–µ–≥–¥–∞ –±—É–¥–µ—Ç –ø—Ä–∞–≤–∏–ª—å–Ω–æ –∑–∞–∫—Ä—ã—Ç–æ, –¥–∞–∂–µ –ø—Ä–∏ –æ—à–∏–±–∫–∞—Ö.
        –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –∑–∞–∫—Ä—ã–≤–∞–µ—Ç —Å–æ–µ–¥–∏–Ω–µ–Ω–∏–µ –∏ –∫—É—Ä—Å–æ—Ä.

        Yields:
            psycopg2.cursor: –ö—É—Ä—Å–æ—Ä –¥–ª—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è SQL-–∑–∞–ø—Ä–æ—Å–æ–≤

        –ü—Ä–∏–º–µ—Ä –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è:
            with Database.get_cursor() as cursor:
                cursor.execute("SELECT * FROM notes")
                results = cursor.fetchall()
        """
        conn = None
        cursor = None
        try:
            # –ü–æ–ª—É—á–∞–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ –ë–î
            conn = Database.get_connection()
            # –°–æ–∑–¥–∞–µ–º —Å–ø–µ—Ü. –∫—É—Ä—Å–æ—Ä, –∫–æ—Ç–æ—Ä—ã–π –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –¥–∞–Ω–Ω—ã–µ –≤ –≤–∏–¥–µ —Å–ª–æ–≤–∞—Ä—è
            cursor = conn.cursor(cursor_factory=RealDictCursor)
            # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –∫—É—Ä—Å–æ—Ä –≤ –±–ª–æ–∫ with, –æ—Ç–¥–∞–µ–º –µ–≥–æ –Ω–∞—Ä—É–∂—É
            yield cursor
            # –ï—Å–ª–∏ –≤—Å–µ —É—Å–ø–µ—à–Ω–æ, —Ñ–∏–∫—Å–∏—Ä—É–µ–º –∏–∑–º–µ–Ω–µ–Ω–∏—è
            conn.commit()

        except Exception as e:
            # –ï—Å–ª–∏ –ø—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞, –æ—Ç–∫–∞—Ç—ã–≤–∞–µ–º –∏–∑–º–µ–Ω–µ–Ω–∏—è
            if conn:
                conn.rollback()
            print(f"‚ùå –û—à–∏–±–∫–∞ –ë–î: {e}")
            raise  # –ü—Ä–æ–±—Ä–∞—Å—ã–≤–∞–µ–º –∏—Å–∫–ª—é—á–µ–Ω–∏–µ

        finally:
            # –í –ª—é–±–æ–º —Å–ª—É—á–∞–µ –∑–∞–∫—Ä—ã–≤–∞–µ–º –∫—É—Ä—Å–æ—Ä –∏ —Å–æ–µ–¥–∏–Ω–µ–Ω–∏–µ
            if cursor:
                cursor.close()
            if conn:
                conn.close()

    @staticmethod
    def init_database():
        """
        –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ—Ç –±–∞–∑—É –¥–∞–Ω–Ω—ã—Ö: —Å–æ–∑–¥–∞–µ—Ç —Ç–∞–±–ª–∏—Ü—É, –µ—Å–ª–∏ –æ–Ω–∞ –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç.
        –í—ã–∑—ã–≤–∞–µ—Ç—Å—è –ø—Ä–∏ –ø–µ—Ä–≤–æ–º –∑–∞–ø—É—Å–∫–µ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è.
        """
        try:
            with Database.get_cursor() as cursor:
                # –°–æ–∑–¥–∞–µ–º —Ç–∞–±–ª–∏—Ü—É notes, –µ—Å–ª–∏ –æ–Ω–∞ –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
                cursor.execute("""
                    CREATE TABLE IF NOT EXISTS notes (
                        id SERIAL PRIMARY KEY,           -- –ê–≤—Ç–æ–∏–Ω–∫—Ä–µ–º–µ–Ω—Ç–Ω—ã–π –ø–µ—Ä–≤–∏—á–Ω—ã–π –∫–ª—é—á
                        title VARCHAR(255) NOT NULL,     -- –ó–∞–≥–æ–ª–æ–≤–æ–∫ (–º–∞–∫—Å 255 —Å–∏–º–≤–æ–ª–æ–≤)
                        body TEXT NOT NULL,              -- –¢–µ–∫—Å—Ç –∑–∞–º–µ—Ç–∫–∏
                        status VARCHAR(20) NOT NULL DEFAULT 'todo',  -- –°—Ç–∞—Ç—É—Å —Å–æ –∑–Ω–∞—á–µ–Ω–∏–µ–º –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é
                        priority VARCHAR(20) NOT NULL DEFAULT 'medium',  -- –ü—Ä–∏–æ—Ä–∏—Ç–µ—Ç —Å–æ –∑–Ω–∞—á–µ–Ω–∏–µ–º –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é
                        created TIMESTAMP DEFAULT CURRENT_TIMESTAMP,  -- –î–∞—Ç–∞ —Å–æ–∑–¥–∞–Ω–∏—è: —Ö—Ä–∞–Ω–∏—Ç –¥–∞—Ç—É –∏ –≤—Ä–µ–º—è, –ø–æ–¥—Å—Ç–∞–≤–ª—è–µ—Ç –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –∑–Ω–∞—á–µ–Ω–∏–µ, –∏ –±–µ—Ä–µ—Ç —Ç–µ–∫—É—â–µ–µ –≤—Ä–µ–º—è
                        updated TIMESTAMP DEFAULT CURRENT_TIMESTAMP   -- –î–∞—Ç–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è (—Ç–µ–∫—É—â–µ–µ –≤—Ä–µ–º—è)
                    )
                """)

                # –°–æ–∑–¥–∞–µ–º –ø–æ–ª–Ω–æ—Ç–µ–∫—Å—Ç–æ–≤—ã–π –∏–Ω–¥–µ–∫—Å –¥–ª—è –±—ã—Å—Ç—Ä–æ–≥–æ –ø–æ–∏—Å–∫–∞
                # GIN –∏–Ω–¥–µ–∫—Å —É—Å–∫–æ—Ä—è–µ—Ç –ø–æ–∏—Å–∫ –ø–æ —Ç–µ–∫—Å—Ç—É
                # to_tsvector('russian', ...) - –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç —Ç–µ–∫—Å—Ç –≤ –≤–µ–∫—Ç–æ—Ä–∞ –¥–ª—è —Ä—É—Å—Å–∫–æ–≥–æ —è–∑—ã–∫–∞, –æ–±—ä–µ–¥–∏–Ω—è–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ –∏ —Ç–µ–∫—Å—Ç –∑–∞–º–µ—Ç–∫–∏ –¥–ª—è –ø–æ–∏—Å–∫–∞ –ø–æ –æ–±–æ–∏–º –ø–æ–ª—è–º
                cursor.execute("""
                    CREATE INDEX IF NOT EXISTS idx_notes_search 
                    ON notes USING gin(to_tsvector('russian', title || ' ' || body))
                """)
                print("‚úÖ –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä–æ–≤–∞–Ω–∞")

        except Exception as e:
            print(f"‚ùå –û—à–∏–±–∫–∞ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ –ë–î: {e}")
            raise


